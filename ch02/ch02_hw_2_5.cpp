#include <iostream>
using namespace std;

int main() {
	cout << 'a' 	<< endl;
	cout << L'a' 	<< endl;
	cout << "a" 	<< endl;
	cout << L"a" 	<< endl;

	cout << endl;

	cout << 10	<< endl;
	cout << 10u	<< endl;
	cout << 10ul	<< endl;
	cout << 012 << endl;
	cout << 0xa << endl;

	cout << endl;

	cout << 3.14 	<< endl;
	cout << 3.14f	<< endl;
	cout << 3.14L	<< endl;

	cout << endl;

	cout << 100		<< endl;
	cout << 100u		<< endl;
	cout << 100.		<< endl;
	cout << 10e+1	<< endl;

 	return 0;
}